# Терминал Linux — шпаргалка

# Навигация и работа с папками
# pwd — Показать текущую директорию
# ls — Список файлов и папок
# ls -l — Список с правами, владельцем, размером, датой
# ls -a — Показать скрытые файлы (начинаются с .)
# ls -lh — Удобный для чтения размер (KB, MB)
# cd ПАПКА — Перейти в указанную папку
# cd .. — На уровень выше
# cd ~ — В домашнюю папку
# cd / — В корень файловой системы

# Создание и удаление
# mkdir имя — Создать папку
# mkdir -p a/b/c — Создать вложенные папки
# rmdir имя — Удалить пустую папку
# rm -r имя  — Удалить папку с содержимым

# Работа с файлами
# touch файл.txt — Создать пустой файл
# echo "Текст" > файл.txt — Записать текст в файл (перезаписать)
# echo "Текст" >> файл.txt — Добавить текст в конец файла
# cat файл.txt — Показать содержимое
# more файл.txt — Просмотр постранично
# less файл.txt — Просмотр с прокруткой (q — выход)
# nano файл.txt — Редактировать файл в nano
# cp файл1 файл2 — Копировать файл
# mv старое новое — Переименовать или переместить
# rm файл.txt — Удалить файл

# Поиск и фильтрация
# grep слово файл.txt — Найти слово в файле
# grep -i слово файл.txt — Поиск без учёта регистра
# grep -r слово папка — Поиск во всех файлах папки
# find . -name "*.txt" — Найти все .txt файлы
# Информация о системе и пользователях
# whoami — текущий пользователь
# hostname — Имя компьютера
# date — Дата и время
# uname -a — Информация о системе
# uptime — Время работы системы
# df -h — Свободное место на диске
# du -sh папка — Размер папки
# history — История команд

# Права доступа
# ls -l — Посмотреть права
# chmod 644 файл — Права: владелец — чтение+запись, остальные — чтение
# chmod +x файл — Сделать файл исполняемым

# Полезно
# clear — Очистить экран
# exit — Выйти из терминала
# man команда — Справка по команде
# ctrl + c — Прервать выполнение команды
# ctrl + l — Очистить экран (как clear)
# tab —  Автодополнение имени файла/папки

# Совет:
