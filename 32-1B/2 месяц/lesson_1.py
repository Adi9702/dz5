
# ООП (Object-Oriented Programming) — это стиль программирования, где всё строится вокруг объектов.
# Объект = это как вещь в реальном мире (машина, телефон, человек), у него есть:

# Состояние (данные) → атрибуты, свойства.

# Поведение (действия) → методы (функции внутри класса).

# Чтобы создавать объекты, мы используем классы — это как «чертёж».

# Класс = план, Объект = экземпляр по плану.


# 4 главных принципа ООП

# Инкапсуляция

# Скрытие внутренней реализации объекта.
# Мы управляем доступом через геттеры, сеттеры, приватные атрибуты.
# Пример: телефон — мы видим экран, но не влезаем внутрь кода ОС.

# Наследование

# Один класс может наследовать свойства и методы другого.
# Позволяет переиспользовать код.
# Пример: класс Car → от него можно создать ElectricCar, Truck.

# Полиморфизм

# Разные объекты могут выполнять одинаковые действия по-разному.
# Пример: метод sound() у животных — собака лает, кошка мяукает.

# Абстракция

# Мы выделяем только важные характеристики объекта и скрываем лишнее.
# Пример: водитель знает, что у машины есть руль и педали, но не обязан знать, как работает двигатель.



# Класс = чертёж автомобиля.
class Car:
    pass
# Здесь Car — это класс.

# Объект = конкретная машина (например, твоя красная Toyota).
car1 = Car()
car2 = Car()
# car1 и car2 — это разные объекты класса Car.

# Атрибут = её характеристики (цвет, марка, колёса).
class Car:
    wheels = 4  # атрибут класса

    def __init__(self, brand, color):
        self.brand = brand  # атрибут объекта
        self.color = color

car1 = Car("Toyota", "red")
car2 = Car("BMW", "black")

print(car1.brand, car1.color)  # Toyota red
print(car2.brand, car2.color)  # BMW black
print(car1.wheels, car2.wheels)  # 4 4 (одно значение для всех)

# Метод = действия машины (ехать, тормозить, сигналить).
class Car:
    def __init__(self, brand, color):
        self.brand = brand
        self.color = color

    def drive(self):  # метод
        print(f"{self.brand} поехала!")

car1 = Car("Toyota", "red")
car1.drive()  # Toyota поехала!